package java102lecture.A05Collections.lecture_5_4_Tree_Set;

public class Student {

    private String name;

    private int note;

    public Student(String name, int note) {
        this.name = name;
        this.note = note;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNote() {
        return note;
    }

    public void setNote(int note) {
        this.note = note;
    }
}
